package com.revature.beans;

public class Feature {
	
	private Integer id; 
	private String name; 
	
	public Feature() {}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
