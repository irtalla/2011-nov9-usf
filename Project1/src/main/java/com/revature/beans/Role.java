package com.revature.beans;

public class Role {
	private Integer id = null;
	private String name = null; 
	
	public Role() {}
	

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}
	
	
}
